#include "push_swap.h"

void pa(t_brain *Brain)
{
	int	i;

	i = Brain->alen - 1;
	if (Brain->blen != 0)
	{
		while (i > 0)
		{
			Brain->stacka[i] = Brain->stacka[i + 1];
			i--;
		}
	}
}
